using UnityEngine;

public class PlayerMovement : MonoBehaviour
{
    public float speed = 5f; // Vitesse de déplacement
    public float jumpForce = 5f; // Force de saut
    public CharacterController controller; // Référence au CharacterController
    public Transform groundCheck; // Position pour vérifier le sol
    public float groundDistance = 0.4f; // Rayon de la vérification du sol
    public LayerMask groundMask; // Layer des objets considérés comme sol

    private Vector3 velocity; // Stocke la vitesse actuelle
    private bool isGrounded; // Vérifie si le joueur est au sol

    void Update()
    {
        // Vérifie si le joueur est au sol
        isGrounded = Physics.CheckSphere(groundCheck.position, groundDistance, groundMask);

        if (isGrounded && velocity.y < 0)
        {
            velocity.y = -2f; // Réinitialise la vélocité verticale si au sol
        }

        // Récupère les entrées de déplacement
        float x = Input.GetAxis("Horizontal");
        float z = Input.GetAxis("Vertical");

        // Calculer la direction
        Vector3 move = transform.right * x + transform.forward * z;

        // Appliquer le déplacement
        controller.Move(move * speed * Time.deltaTime);

        // Gestion du saut
        if (Input.GetButtonDown("Jump") && isGrounded)
        {
            velocity.y = Mathf.Sqrt(jumpForce * -2f * Physics.gravity.y);
        }

        // Appliquer la gravité
        velocity.y += Physics.gravity.y * Time.deltaTime;
        controller.Move(velocity * Time.deltaTime);
    }
}








